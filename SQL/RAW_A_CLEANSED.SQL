--Esto lo ejecuto para la creacion de las tablas en CLEANSED, los STREAMS y las TASKS de cada una de ellas. 
USE DATABASE PROYECTO;
USE SCHEMA STREAMS_Y_TASKS;


-- Creo la tabla con los datos limpios que cumplen las condiciones
CREATE TABLE IF NOT EXISTS CLEANSED_MEDICARE.CATEGORIA_EQUIPOS AS 
SELECT * FROM RAW_MEDICARE.CATEGORIA_EQUIPOS WHERE 1=0;

-- Creacion de la tabla de los datos que fallaron las reglas de calidad y también me creo una columna con para comentar el motivo del error.
CREATE TABLE IF NOT EXISTS CLEANSED_MEDICARE.CATEGORIA_EQUIPOS_ERRORS AS 
SELECT *, 'MOTIVO_ERROR_PLACEHOLDER'::VARCHAR(255) AS MOTIVO_ERROR 
FROM RAW_MEDICARE.CATEGORIA_EQUIPOS WHERE 1=0;


-- para vaciar las tablas por si hace una ejecucion futura 
TRUNCATE TABLE CLEANSED_MEDICARE.CATEGORIA_EQUIPOS;
TRUNCATE TABLE CLEANSED_MEDICARE.CATEGORIA_EQUIPOS_ERRORS;


-- Cargo el historico de datos desde la capa RAW 
INSERT ALL
  -- aplico la regla de calidad para esa tabla
  WHEN (CODIGO_CATEGORIA IS NOT NULL AND NOMBRE_CATEGORIA IS NOT NULL) THEN
    INTO CLEANSED_MEDICARE.CATEGORIA_EQUIPOS
    VALUES (ID_CATEGORIA, CODIGO_CATEGORIA, NOMBRE_CATEGORIA, SUBCATEGORIA, DESCRIPCION, FECHA_CREACION, FECHA_MODIFICACION, ID_CARGA, _DLT_LOAD_ID, _DLT_ID)
  
-- si no cumple las reglas de calidad, lo envío a la tabla errores 
  ELSE
    INTO CLEANSED_MEDICARE.CATEGORIA_EQUIPOS_ERRORS
    VALUES (ID_CATEGORIA, CODIGO_CATEGORIA, NOMBRE_CATEGORIA, SUBCATEGORIA, DESCRIPCION, FECHA_CREACION, FECHA_MODIFICACION, ID_CARGA, _DLT_LOAD_ID, _DLT_ID,
            CASE 
                WHEN CODIGO_CATEGORIA IS NULL AND NOMBRE_CATEGORIA IS NULL THEN 'Falla Regla: CODIGO y NOMBRE son nulos'
                WHEN CODIGO_CATEGORIA IS NULL THEN 'Falla Regla: CODIGO_CATEGORIA es nulo'
                WHEN NOMBRE_CATEGORIA IS NULL THEN 'Falla Regla: NOMBRE_CATEGORIA es nulo'
            END)


--Lectura única de la tabla RAW origen
SELECT ID_CATEGORIA, CODIGO_CATEGORIA, NOMBRE_CATEGORIA, SUBCATEGORIA, DESCRIPCION, FECHA_CREACION, FECHA_MODIFICACION, ID_CARGA, _DLT_LOAD_ID, _DLT_ID
FROM RAW_MEDICARE.CATEGORIA_EQUIPOS;


--Ahora, voy a implementar el auto-incremental 

--Creo el stream, que me graba sólo los inserts nuevos que llegan a RAW 
CREATE OR REPLACE STREAM STREAM_CLEANSED_MEDICARE_CATEGORIA_EQUIPOS 
ON TABLE RAW_MEDICARE.CATEGORIA_EQUIPOS
APPEND_ONLY = TRUE; 

--Creo el TASK, que revisa el stream cada 5 min y aplico las reglas de calidad de nuevo como habia hecho antes con los datos del histórico de la capa RAW.  
CREATE OR REPLACE TASK TASK_CLEANSED_MEDICARE_CATEGORIA_EQUIPOS
  WAREHOUSE = COMPUTE_WH
  SCHEDULE = '5 MINUTE'
  WHEN SYSTEM$STREAM_HAS_DATA('STREAM_CLEANSED_MEDICARE_CATEGORIA_EQUIPOS')
AS
  INSERT ALL
    WHEN (CODIGO_CATEGORIA IS NOT NULL AND NOMBRE_CATEGORIA IS NOT NULL) THEN
      INTO CLEANSED_MEDICARE.CATEGORIA_EQUIPOS
      VALUES (ID_CATEGORIA, CODIGO_CATEGORIA, NOMBRE_CATEGORIA, SUBCATEGORIA, DESCRIPCION, FECHA_CREACION, FECHA_MODIFICACION, ID_CARGA, _DLT_LOAD_ID, _DLT_ID)
    ELSE
      INTO CLEANSED_MEDICARE.CATEGORIA_EQUIPOS_ERRORS
      VALUES (ID_CATEGORIA, CODIGO_CATEGORIA, NOMBRE_CATEGORIA, SUBCATEGORIA, DESCRIPCION, FECHA_CREACION, FECHA_MODIFICACION, ID_CARGA, _DLT_LOAD_ID, _DLT_ID,
              CASE 
                  WHEN CODIGO_CATEGORIA IS NULL AND NOMBRE_CATEGORIA IS NULL THEN 'Falla Regla: CODIGO y NOMBRE son nulos'
                  WHEN CODIGO_CATEGORIA IS NULL THEN 'Falla Regla: CODIGO_CATEGORIA es nulo'
                  WHEN NOMBRE_CATEGORIA IS NULL THEN 'Falla Regla: NOMBRE_CATEGORIA es nulo'
              END)


--Hago una lectura exclusiva desde el Stream para procesar solo lo nuevos datos
  SELECT ID_CATEGORIA, CODIGO_CATEGORIA, NOMBRE_CATEGORIA, SUBCATEGORIA, DESCRIPCION, FECHA_CREACION, FECHA_MODIFICACION, ID_CARGA, _DLT_LOAD_ID, _DLT_ID
  FROM STREAM_CLEANSED_MEDICARE_CATEGORIA_EQUIPOS
  WHERE METADATA$ACTION = 'INSERT';

--Activo el automatismo
ALTER TASK TASK_CLEANSED_MEDICARE_CATEGORIA_EQUIPOS RESUME;