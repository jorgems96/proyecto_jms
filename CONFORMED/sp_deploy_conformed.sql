-- sp_deploy_conformed.sql
-- Crea el esquema, todas las tablas de la capa CONFORMED y el SP de carga inicial.
-- Las tablas se crean vacias; los datos se cargan mediante SP_DEPLOY_CONFORMED().
--
-- Prerequisitos (orden en _FICHEROS_SETUP):
--   1. sp_merge_utils.sql    → SP_MERGE_SCD1 y SP_MERGE_SCD1_SCD2
--   2. sp_deploy_conformed.sql (este fichero) → DDL + SP_DEPLOY_CONFORMED
--   3. sp_load_conformed.sql  → SP_LOAD_* para incrementales via Task DAG
--
-- Carga inicial:
--   CALL CONFORMED.SP_DEPLOY_CONFORMED()

CREATE SCHEMA IF NOT EXISTS CONFORMED;


