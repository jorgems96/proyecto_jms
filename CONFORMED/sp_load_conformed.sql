-- DIM_PERFIL_MANTENIMIENTO: sin stream (generada por CROSS JOIN de valores fijos)
CREATE OR REPLACE PROCEDURE CONFORMED.SP_LOAD_DIM_PERFIL_MANTENIMIENTO()
RETURNS STRING
LANGUAGE SQL
AS
$$
BEGIN
    INSERT INTO CONFORMED.DIM_PERFIL_MANTENIMIENTO (
        TIPO_MANTENIMIENTO, PRIORIDAD, GRAVEDAD, ES_GARANTIA, REQUIERE_PARADA_EQUIPO
    )
    SELECT tipo, prioridad, gravedad, garantia, parada
    FROM (VALUES ('Preventivo'), ('Correctivo'), ('Calibracion'), ('Inspeccion')) AS t(tipo)
    CROSS JOIN (VALUES ('Baja'), ('Media'), ('Alta'), ('Critica'))               AS p(prioridad)
    CROSS JOIN (VALUES ('Leve'), ('Moderada'), ('Grave'))                        AS g(gravedad)
    CROSS JOIN (VALUES (TRUE), (FALSE))                                          AS ga(garantia)
    CROSS JOIN (VALUES (TRUE), (FALSE))                                          AS pa(parada)
    WHERE NOT EXISTS (
        SELECT SK_PERFIL_MANTENIMIENTO FROM CONFORMED.DIM_PERFIL_MANTENIMIENTO
        WHERE TIPO_MANTENIMIENTO     = tipo
          AND PRIORIDAD              = prioridad
          AND GRAVEDAD               = gravedad
          AND ES_GARANTIA            = garantia
          AND REQUIERE_PARADA_EQUIPO = parada
    );

    RETURN 'DIM_PERFIL_MANTENIMIENTO OK';
END;
$$;


-- DIM_PERFIL_ENSAYO: sin stream (generada por CROSS JOIN de valores fijos)
CREATE OR REPLACE PROCEDURE CONFORMED.SP_LOAD_DIM_PERFIL_ENSAYO()
RETURNS STRING
LANGUAGE SQL
AS
$$
BEGIN
    INSERT INTO CONFORMED.DIM_PERFIL_ENSAYO (
        FASE_ENSAYO, TIPO_CIEGO, TIPO_ALEATORIZACION, GRUPO_TRATAMIENTO, VIA_ADMINISTRACION
    )
    SELECT fase, ciego, aleatorizacion, grupo, via
    FROM (VALUES ('Fase I'), ('Fase II'), ('Fase III'), ('Fase IV'))                   AS f(fase)
    CROSS JOIN (VALUES ('Abierto'), ('Simple Ciego'), ('Doble Ciego'))                 AS c(ciego)
    CROSS JOIN (VALUES ('Aleatorizado'), ('No Aleatorizado'))                          AS a(aleatorizacion)
    CROSS JOIN (VALUES ('Experimental'), ('Control'), ('Placebo'))                     AS g(grupo)
    CROSS JOIN (VALUES ('Oral'), ('Intravenosa'), ('Subcutanea'), ('Topica'), ('Inhalatoria')) AS v(via)
    WHERE NOT EXISTS (
        SELECT SK_PERFIL_ENSAYO FROM CONFORMED.DIM_PERFIL_ENSAYO
        WHERE FASE_ENSAYO         = fase
          AND TIPO_CIEGO          = ciego
          AND TIPO_ALEATORIZACION = aleatorizacion
          AND GRUPO_TRATAMIENTO   = grupo
          AND VIA_ADMINISTRACION  = via
    );

    RETURN 'DIM_PERFIL_ENSAYO OK';
END;
$$;


