CREATE OR REPLACE PROCEDURE CONFORMED.SP_LOAD_DIM_MEDICO()
RETURNS STRING
LANGUAGE SQL
AS
$$
BEGIN
    -- JOIN con tablas secundarias al materializar (no al leer el stream dos veces)
    CREATE OR REPLACE TEMPORARY TABLE CONFORMED._TMP_MEDICOS AS
    SELECT s.ID_MEDICO, s.NOMBRE,
           s.APELLIDO1 || ' ' || s.APELLIDO2 AS APELLIDOS,
           s.NUMERO_COLEGIADO, s.TELEFONO, s.EMAIL,
           COALESCE(esp.NOMBRE_ESPECIALIDAD, 'Sin especialidad') AS ESPECIALIDAD,
           dep.NOMBRE_DEPARTAMENTO AS DEPARTAMENTO
    FROM CONFORMED.STREAM_MEDICOS s
    LEFT JOIN CLEANSED.ESPECIALIDADES esp ON esp.ID_ESPECIALIDAD     = s.ID_ESPECIALIDAD
    LEFT JOIN CLEANSED.DEPARTAMENTOS  dep ON dep.CODIGO_DEPARTAMENTO = s.CODIGO_DEPARTAMENTO
    WHERE s.METADATA$ACTION = 'INSERT';

    CALL CONFORMED.SP_MERGE_SCD1_SCD2(
        'CONFORMED.DIM_MEDICO',
        'SELECT ID_MEDICO, NOMBRE, APELLIDOS, NUMERO_COLEGIADO,
                TELEFONO, EMAIL, ESPECIALIDAD, DEPARTAMENTO
         FROM CONFORMED._TMP_MEDICOS',
        'ID_MEDICO',
        'NOMBRE,APELLIDOS,NUMERO_COLEGIADO,TELEFONO,EMAIL',
        'ESPECIALIDAD,DEPARTAMENTO'
    );
    RETURN 'DIM_MEDICO OK';
END;
$$;


