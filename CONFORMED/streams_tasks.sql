      AND t.ES_ACTUAL   = TRUE
      AND (t.DIRECCION     IS DISTINCT FROM s.DIRECCION     OR
           t.CIUDAD        IS DISTINCT FROM s.CIUDAD        OR
           t.CODIGO_POSTAL IS DISTINCT FROM s.CODIGO_POSTAL OR
           t.SEGURO_MEDICO IS DISTINCT FROM s.SEGURO_MEDICO OR
           t.TIPO_SEGURO   IS DISTINCT FROM s.TIPO_SEGURO);

    INSERT INTO CONFORMED.DIM_PACIENTE (
        ID_PACIENTE, NOMBRE, APELLIDOS, FECHA_NACIMIENTO, GENERO,
        TELEFONO, EMAIL, GRUPO_SANGUINEO,
        DIRECCION, CIUDAD, CODIGO_POSTAL, SEGURO_MEDICO, TIPO_SEGURO,
        FECHA_INICIO, FECHA_FIN, ES_ACTUAL
    )
    SELECT ID_PACIENTE, NOMBRE,
           APELLIDO1 || ' ' || APELLIDO2 AS APELLIDOS,
           FECHA_NACIMIENTO, SEXO AS GENERO,
           TELEFONO_MOVIL AS TELEFONO, EMAIL, GRUPO_SANGUINEO,
           DIRECCION, CIUDAD, CODIGO_POSTAL,
           COMPANIA_SEGURO AS SEGURO_MEDICO, TIPO_COBERTURA AS TIPO_SEGURO,
           CURRENT_DATE, NULL, TRUE
    FROM CLEANSED_MEDICARE.PACIENTES
    WHERE NOT EXISTS (
        SELECT ID_PACIENTE FROM CONFORMED.DIM_PACIENTE
        WHERE  ID_PACIENTE = CLEANSED_MEDICARE.PACIENTES.ID_PACIENTE
          AND  ES_ACTUAL   = TRUE
    );

    RETURN 'DIM_PACIENTE OK';
END;
$$;


CREATE OR REPLACE PROCEDURE CONFORMED.SP_LOAD_DIM_EQUIPO()
RETURNS STRING
LANGUAGE SQL
AS
$$
BEGIN
    UPDATE CONFORMED.DIM_EQUIPO t
    SET
        NOMBRE_EQUIPO      = s.NOMBRE_EQUIPO,
        DESCRIPCION        = s.DESCRIPCION,
        NUMERO_SERIE       = s.NUMERO_SERIE,
        MODELO             = s.MODELO,
        FECHA_ADQUISICION  = s.FECHA_ADQUISICION,
        FECHA_FIN_GARANTIA = s.FECHA_FIN_GARANTIA,
        CLASE_RIESGO       = s.CLASE_RIESGO
    FROM (
        SELECT ID_EQUIPO, NOMBRE_EQUIPO, DESCRIPCION, NUMERO_SERIE, MODELO,
               FECHA_ADQUISICION, FECHA_FIN_GARANTIA, CLASE_RIESGO
        FROM CLEANSED_MEDICARE.EQUIPOS
    ) s
    WHERE t.ID_EQUIPO = s.ID_EQUIPO
      AND t.ES_ACTUAL = TRUE
      AND (t.NOMBRE_EQUIPO      IS DISTINCT FROM s.NOMBRE_EQUIPO      OR
           t.DESCRIPCION        IS DISTINCT FROM s.DESCRIPCION        OR
           t.NUMERO_SERIE       IS DISTINCT FROM s.NUMERO_SERIE       OR
           t.MODELO             IS DISTINCT FROM s.MODELO             OR
           t.FECHA_ADQUISICION  IS DISTINCT FROM s.FECHA_ADQUISICION  OR
           t.FECHA_FIN_GARANTIA IS DISTINCT FROM s.FECHA_FIN_GARANTIA OR
           t.CLASE_RIESGO       IS DISTINCT FROM s.CLASE_RIESGO);

    UPDATE CONFORMED.DIM_EQUIPO t
    SET FECHA_FIN = CURRENT_DATE - 1, ES_ACTUAL = FALSE
    FROM (
        SELECT e.ID_EQUIPO,
               CASE e.ESTADO_CODIGO
                   WHEN 1 THEN 'Activo'          WHEN 2 THEN 'En Reparacion'
                   WHEN 3 THEN 'Fuera de Servicio' WHEN 4 THEN 'Baja'
               END                              AS ESTADO_EQUIPO,
               d.NOMBRE_DEPARTAMENTO            AS DEPARTAMENTO_ACTUAL,
               u.EDIFICIO || ' - ' || u.SALA    AS UBICACION_ACTUAL
        FROM CLEANSED_MEDICARE.EQUIPOS e
        LEFT JOIN CLEANSED_MEDICARE.DEPARTAMENTOS d ON e.ID_DEPARTAMENTO = d.ID_DEPARTAMENTO
        LEFT JOIN CLEANSED_MEDICARE.UBICACIONES   u ON e.ID_UBICACION    = u.ID_UBICACION
    ) s
    WHERE t.ID_EQUIPO = s.ID_EQUIPO
      AND t.ES_ACTUAL = TRUE
      AND (t.ESTADO_EQUIPO       IS DISTINCT FROM s.ESTADO_EQUIPO       OR
           t.DEPARTAMENTO_ACTUAL IS DISTINCT FROM s.DEPARTAMENTO_ACTUAL OR
           t.UBICACION_ACTUAL    IS DISTINCT FROM s.UBICACION_ACTUAL);

    INSERT INTO CONFORMED.DIM_EQUIPO (
