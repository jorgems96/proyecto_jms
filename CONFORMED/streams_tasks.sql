AS
$$
BEGIN
    CALL CONFORMED.SP_MERGE_SCD1_SCD2(
        'CONFORMED.DIM_MEDICO',
        'SELECT m.ID_MEDICO, m.NOMBRE,
                m.APELLIDO1 || '' '' || m.APELLIDO2 AS APELLIDOS,
                m.NUMERO_COLEGIADO, m.TELEFONO, m.EMAIL,
                esp.NOMBRE_ESPECIALIDAD AS ESPECIALIDAD,
                dep.NOMBRE_DEPARTAMENTO AS DEPARTAMENTO
