CREATE OR REPLACE PROCEDURE CONFORMED.SP_LOAD_DIM_MEDICO()
RETURNS STRING
LANGUAGE SQL
AS
$$
BEGIN
    UPDATE CONFORMED.DIM_MEDICO t
    SET
        NOMBRE           = s.NOMBRE,
        APELLIDOS        = s.APELLIDOS,
        NUMERO_COLEGIADO = s.NUMERO_COLEGIADO,
        TELEFONO         = s.TELEFONO,
        EMAIL            = s.EMAIL
    FROM (
        SELECT m.ID_MEDICO, m.NOMBRE,
               m.APELLIDO1 || ' ' || m.APELLIDO2 AS APELLIDOS,
               m.NUMERO_COLEGIADO, m.TELEFONO, m.EMAIL
        FROM CLEANSED_MEDICARE.MEDICOS m
    ) s
    WHERE t.ID_MEDICO = s.ID_MEDICO
      AND t.ES_ACTUAL = TRUE
      AND (t.NOMBRE           IS DISTINCT FROM s.NOMBRE           OR
           t.APELLIDOS        IS DISTINCT FROM s.APELLIDOS        OR
           t.NUMERO_COLEGIADO IS DISTINCT FROM s.NUMERO_COLEGIADO OR
           t.TELEFONO         IS DISTINCT FROM s.TELEFONO         OR
           t.EMAIL            IS DISTINCT FROM s.EMAIL);

    UPDATE CONFORMED.DIM_MEDICO t
    SET FECHA_FIN = CURRENT_DATE - 1, ES_ACTUAL = FALSE
    FROM (
        SELECT m.ID_MEDICO,
               esp.NOMBRE_ESPECIALIDAD AS ESPECIALIDAD,
               dep.NOMBRE_DEPARTAMENTO AS DEPARTAMENTO
        FROM CLEANSED_MEDICARE.MEDICOS m
        LEFT JOIN CLEANSED_MEDICARE.ESPECIALIDADES esp ON esp.ID_ESPECIALIDAD     = m.ID_ESPECIALIDAD
        LEFT JOIN CLEANSED_MEDICARE.DEPARTAMENTOS  dep ON dep.CODIGO_DEPARTAMENTO = m.CODIGO_DEPARTAMENTO
    ) s
    WHERE t.ID_MEDICO = s.ID_MEDICO
      AND t.ES_ACTUAL = TRUE
      AND (t.ESPECIALIDAD IS DISTINCT FROM s.ESPECIALIDAD OR
           t.DEPARTAMENTO IS DISTINCT FROM s.DEPARTAMENTO);

    INSERT INTO CONFORMED.DIM_MEDICO (
        ID_MEDICO, NOMBRE, APELLIDOS, NUMERO_COLEGIADO,
        TELEFONO, EMAIL, ESPECIALIDAD, DEPARTAMENTO,
        FECHA_INICIO, FECHA_FIN, ES_ACTUAL
    )
    SELECT m.ID_MEDICO, m.NOMBRE,
           m.APELLIDO1 || ' ' || m.APELLIDO2 AS APELLIDOS,
           m.NUMERO_COLEGIADO, m.TELEFONO, m.EMAIL,
           esp.NOMBRE_ESPECIALIDAD AS ESPECIALIDAD,
           dep.NOMBRE_DEPARTAMENTO AS DEPARTAMENTO,
           CURRENT_DATE, NULL, TRUE
    FROM CLEANSED_MEDICARE.MEDICOS m
    LEFT JOIN CLEANSED_MEDICARE.ESPECIALIDADES esp ON esp.ID_ESPECIALIDAD     = m.ID_ESPECIALIDAD
    LEFT JOIN CLEANSED_MEDICARE.DEPARTAMENTOS  dep ON dep.CODIGO_DEPARTAMENTO = m.CODIGO_DEPARTAMENTO
    WHERE NOT EXISTS (
        SELECT ID_MEDICO FROM CONFORMED.DIM_MEDICO
        WHERE  ID_MEDICO = m.ID_MEDICO AND ES_ACTUAL = TRUE
    );

    RETURN 'DIM_MEDICO OK';
END;
$$;


CREATE OR REPLACE PROCEDURE CONFORMED.SP_LOAD_DIM_TECNICO()
RETURNS STRING
LANGUAGE SQL
AS
$$
BEGIN
    UPDATE CONFORMED.DIM_TECNICO t
    SET
        NOMBRE            = s.NOMBRE,
        ESPECIALIDAD      = s.ESPECIALIDAD,
        ANIOS_EXPERIENCIA = s.ANIOS_EXPERIENCIA,
        TELEFONO          = s.TELEFONO,
        EMAIL             = s.EMAIL
    FROM (
        SELECT ID_TECNICO, NOMBRE_TECNICO AS NOMBRE, ESPECIALIDAD,
               ANIOS_EXPERIENCIA, TELEFONO_MOVIL AS TELEFONO, EMAIL_CORPORATIVO AS EMAIL
        FROM CLEANSED_MEDICARE.TECNICOS
    ) s
    WHERE t.ID_TECNICO = s.ID_TECNICO
      AND t.ES_ACTUAL  = TRUE
      AND (t.NOMBRE            IS DISTINCT FROM s.NOMBRE            OR
           t.ESPECIALIDAD      IS DISTINCT FROM s.ESPECIALIDAD      OR
           t.ANIOS_EXPERIENCIA IS DISTINCT FROM s.ANIOS_EXPERIENCIA OR
           t.TELEFONO          IS DISTINCT FROM s.TELEFONO          OR
           t.EMAIL             IS DISTINCT FROM s.EMAIL);

    UPDATE CONFORMED.DIM_TECNICO t
    SET FECHA_FIN = CURRENT_DATE - 1, ES_ACTUAL = FALSE
    FROM (
        SELECT tc.ID_TECNICO,
               dep.NOMBRE_DEPARTAMENTO AS DEPARTAMENTO,
               CASE tc.NIVEL_CERTIFICACION
                   WHEN 1 THEN 'Junior'  WHEN 2 THEN 'Senior'
                   WHEN 3 THEN 'Especialista' WHEN 4 THEN 'Jefe'
               END AS NIVEL_CERTIFICACION
        FROM CLEANSED_MEDICARE.TECNICOS tc
        LEFT JOIN CLEANSED_MEDICARE.DEPARTAMENTOS dep ON dep.ID_DEPARTAMENTO = tc.ID_DEPARTAMENTO
    ) s
    WHERE t.ID_TECNICO = s.ID_TECNICO
      AND t.ES_ACTUAL  = TRUE
      AND (t.DEPARTAMENTO        IS DISTINCT FROM s.DEPARTAMENTO        OR
           t.NIVEL_CERTIFICACION IS DISTINCT FROM s.NIVEL_CERTIFICACION);

    INSERT INTO CONFORMED.DIM_TECNICO (
        ID_TECNICO, NOMBRE, ESPECIALIDAD, ANIOS_EXPERIENCIA,
        TELEFONO, EMAIL, DEPARTAMENTO, NIVEL_CERTIFICACION,
        FECHA_INICIO, FECHA_FIN, ES_ACTUAL
    )
    SELECT tc.ID_TECNICO, tc.NOMBRE_TECNICO AS NOMBRE, tc.ESPECIALIDAD,
           tc.ANIOS_EXPERIENCIA, tc.TELEFONO_MOVIL AS TELEFONO,
           tc.EMAIL_CORPORATIVO AS EMAIL,
           dep.NOMBRE_DEPARTAMENTO AS DEPARTAMENTO,
           CASE tc.NIVEL_CERTIFICACION
               WHEN 1 THEN 'Junior'  WHEN 2 THEN 'Senior'
               WHEN 3 THEN 'Especialista' WHEN 4 THEN 'Jefe'
           END AS NIVEL_CERTIFICACION,
           CURRENT_DATE, NULL, TRUE
    FROM CLEANSED_MEDICARE.TECNICOS tc
    LEFT JOIN CLEANSED_MEDICARE.DEPARTAMENTOS dep ON dep.ID_DEPARTAMENTO = tc.ID_DEPARTAMENTO
    WHERE NOT EXISTS (
        SELECT ID_TECNICO FROM CONFORMED.DIM_TECNICO
        WHERE  ID_TECNICO = tc.ID_TECNICO AND ES_ACTUAL = TRUE
    );

    RETURN 'DIM_TECNICO OK';
END;
$$;


CREATE OR REPLACE PROCEDURE CONFORMED.SP_LOAD_DIM_PACIENTE()
RETURNS STRING
LANGUAGE SQL
AS
$$
BEGIN
    UPDATE CONFORMED.DIM_PACIENTE t
    SET
        NOMBRE           = s.NOMBRE,
