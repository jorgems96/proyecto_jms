CREATE OR REPLACE PROCEDURE CONFORMED.SP_LOAD_DIM_FECHA()
RETURNS STRING
LANGUAGE SQL
AS
$$
BEGIN
    CREATE OR REPLACE TABLE CONFORMED.DIM_FECHA AS
    WITH SERIE AS (
        SELECT DATEADD(DAY, SEQ4(), '2015-01-01'::DATE) AS FECHA
        FROM TABLE(GENERATOR(ROWCOUNT => 5844))
    )
    SELECT
        TO_NUMBER(TO_CHAR(FECHA, 'YYYYMMDD'))                   AS SK_FECHA,
        FECHA                                                   AS FECHA,
        YEAR(FECHA)                                             AS ANIO,
        CASE WHEN MONTH(FECHA) <= 6 THEN 1 ELSE 2 END          AS SEMESTRE,
        QUARTER(FECHA)                                          AS TRIMESTRE,
        MONTH(FECHA)                                            AS MES,
        CASE MONTH(FECHA)
            WHEN 1  THEN 'Enero'        WHEN 2  THEN 'Febrero'
            WHEN 3  THEN 'Marzo'        WHEN 4  THEN 'Abril'
            WHEN 5  THEN 'Mayo'         WHEN 6  THEN 'Junio'
            WHEN 7  THEN 'Julio'        WHEN 8  THEN 'Agosto'
            WHEN 9  THEN 'Septiembre'   WHEN 10 THEN 'Octubre'
            WHEN 11 THEN 'Noviembre'    WHEN 12 THEN 'Diciembre'
        END                                                     AS NOMBRE_MES,
        WEEKISO(FECHA)                                          AS SEMANA_ANIO,
        DAY(FECHA)                                              AS DIA,
        DAYOFWEEKISO(FECHA)                                     AS DIA_SEMANA,
        CASE DAYOFWEEKISO(FECHA)
            WHEN 1 THEN 'Lunes'         WHEN 2 THEN 'Martes'
            WHEN 3 THEN 'Miercoles'     WHEN 4 THEN 'Jueves'
            WHEN 5 THEN 'Viernes'       WHEN 6 THEN 'Sabado'
            WHEN 7 THEN 'Domingo'
        END                                                     AS NOMBRE_DIA,
        DAYOFWEEKISO(FECHA) IN (6, 7)                          AS ES_FIN_SEMANA,
        FALSE                                                   AS ES_FESTIVO
    FROM SERIE;

    ALTER TABLE CONFORMED.DIM_FECHA
        ADD CONSTRAINT PK_DIM_FECHA PRIMARY KEY (SK_FECHA);

    RETURN 'DIM_FECHA OK';
END;
$$;


CREATE OR REPLACE PROCEDURE CONFORMED.SP_LOAD_DIM_DEPARTAMENTO()
RETURNS STRING
LANGUAGE SQL
AS
$$
BEGIN
    WITH JERARQUIA AS (
        SELECT ID_DEPARTAMENTO, ID_DEPARTAMENTO_PADRE, 1 AS NIVEL_JERARQUIA
        FROM CLEANSED_MEDICARE.DEPARTAMENTOS
        WHERE ID_DEPARTAMENTO_PADRE IS NULL
        UNION ALL
        SELECT d.ID_DEPARTAMENTO, d.ID_DEPARTAMENTO_PADRE, j.NIVEL_JERARQUIA + 1
        FROM CLEANSED_MEDICARE.DEPARTAMENTOS d
        JOIN JERARQUIA j ON j.ID_DEPARTAMENTO = d.ID_DEPARTAMENTO_PADRE
    )
    UPDATE CONFORMED.DIM_DEPARTAMENTO t
    SET
        NOMBRE_DEPARTAMENTO   = s.NOMBRE_DEPARTAMENTO,
        ID_DEPARTAMENTO_PADRE = s.ID_DEPARTAMENTO_PADRE,
        SK_DEPARTAMENTO_PADRE = (
            SELECT SK_DEPARTAMENTO FROM CONFORMED.DIM_DEPARTAMENTO
            WHERE  ID_DEPARTAMENTO = s.ID_DEPARTAMENTO_PADRE
        ),
        NIVEL_JERARQUIA       = s.NIVEL_JERARQUIA,
        RESPONSABLE           = s.RESPONSABLE,
        TELEFONO_DEPARTAMENTO = s.TELEFONO_DEPARTAMENTO,
        EMAIL_DEPARTAMENTO    = s.EMAIL_DEPARTAMENTO
    FROM (
        SELECT d.ID_DEPARTAMENTO, d.NOMBRE_DEPARTAMENTO, d.ID_DEPARTAMENTO_PADRE,
               j.NIVEL_JERARQUIA, d.NOMBRE_RESPONSABLE AS RESPONSABLE,
               d.TELEFONO_DEPARTAMENTO, d.EMAIL_DEPARTAMENTO
        FROM CLEANSED_MEDICARE.DEPARTAMENTOS d
        JOIN JERARQUIA j ON j.ID_DEPARTAMENTO = d.ID_DEPARTAMENTO
    ) s
    WHERE t.ID_DEPARTAMENTO = s.ID_DEPARTAMENTO
      AND (t.NOMBRE_DEPARTAMENTO   IS DISTINCT FROM s.NOMBRE_DEPARTAMENTO   OR
           t.ID_DEPARTAMENTO_PADRE IS DISTINCT FROM s.ID_DEPARTAMENTO_PADRE OR
           t.NIVEL_JERARQUIA       IS DISTINCT FROM s.NIVEL_JERARQUIA       OR
           t.RESPONSABLE           IS DISTINCT FROM s.RESPONSABLE           OR
           t.TELEFONO_DEPARTAMENTO IS DISTINCT FROM s.TELEFONO_DEPARTAMENTO OR
           t.EMAIL_DEPARTAMENTO    IS DISTINCT FROM s.EMAIL_DEPARTAMENTO);

    WITH JERARQUIA AS (
        SELECT ID_DEPARTAMENTO, ID_DEPARTAMENTO_PADRE, 1 AS NIVEL_JERARQUIA
        FROM CLEANSED_MEDICARE.DEPARTAMENTOS
        WHERE ID_DEPARTAMENTO_PADRE IS NULL
        UNION ALL
        SELECT d.ID_DEPARTAMENTO, d.ID_DEPARTAMENTO_PADRE, j.NIVEL_JERARQUIA + 1
        FROM CLEANSED_MEDICARE.DEPARTAMENTOS d
        JOIN JERARQUIA j ON j.ID_DEPARTAMENTO = d.ID_DEPARTAMENTO_PADRE
    )
    INSERT INTO CONFORMED.DIM_DEPARTAMENTO (
        ID_DEPARTAMENTO, NOMBRE_DEPARTAMENTO, ID_DEPARTAMENTO_PADRE,
        SK_DEPARTAMENTO_PADRE, NIVEL_JERARQUIA, RESPONSABLE,
        TELEFONO_DEPARTAMENTO, EMAIL_DEPARTAMENTO
    )
    SELECT d.ID_DEPARTAMENTO, d.NOMBRE_DEPARTAMENTO, d.ID_DEPARTAMENTO_PADRE,
           (SELECT SK_DEPARTAMENTO FROM CONFORMED.DIM_DEPARTAMENTO
            WHERE  ID_DEPARTAMENTO = d.ID_DEPARTAMENTO_PADRE),
           j.NIVEL_JERARQUIA, d.NOMBRE_RESPONSABLE AS RESPONSABLE,
           d.TELEFONO_DEPARTAMENTO, d.EMAIL_DEPARTAMENTO
    FROM CLEANSED_MEDICARE.DEPARTAMENTOS d
    JOIN JERARQUIA j ON j.ID_DEPARTAMENTO = d.ID_DEPARTAMENTO
    WHERE NOT EXISTS (
        SELECT ID_DEPARTAMENTO FROM CONFORMED.DIM_DEPARTAMENTO
        WHERE  ID_DEPARTAMENTO = d.ID_DEPARTAMENTO
    );

    UPDATE CONFORMED.DIM_DEPARTAMENTO t
    SET    SK_DEPARTAMENTO_PADRE = p.SK_DEPARTAMENTO
    FROM   CONFORMED.DIM_DEPARTAMENTO p
    WHERE  t.ID_DEPARTAMENTO_PADRE = p.ID_DEPARTAMENTO
      AND  t.SK_DEPARTAMENTO_PADRE IS NULL
      AND  t.ID_DEPARTAMENTO_PADRE IS NOT NULL;

    RETURN 'DIM_DEPARTAMENTO OK';
END;
$$;


CREATE OR REPLACE PROCEDURE CONFORMED.SP_LOAD_DIM_UBICACION()
RETURNS STRING
LANGUAGE SQL
AS
$$
BEGIN
    UPDATE CONFORMED.DIM_UBICACION t
    SET
        EDIFICIO  = s.EDIFICIO,
        PLANTA    = s.PLANTA,
        ALA       = s.ALA,
        SALA      = s.SALA,
        TIPO_SALA = s.TIPO_SALA,
        CAPACIDAD = s.CAPACIDAD
    FROM (
        SELECT ID_UBICACION, EDIFICIO, PLANTA, ALA, SALA, TIPO_SALA, CAPACIDAD
        FROM CLEANSED_MEDICARE.UBICACIONES
    ) s
    WHERE t.ID_UBICACION = s.ID_UBICACION
      AND (t.EDIFICIO  IS DISTINCT FROM s.EDIFICIO  OR
           t.PLANTA    IS DISTINCT FROM s.PLANTA    OR
           t.ALA       IS DISTINCT FROM s.ALA       OR
           t.SALA      IS DISTINCT FROM s.SALA      OR
           t.TIPO_SALA IS DISTINCT FROM s.TIPO_SALA OR
           t.CAPACIDAD IS DISTINCT FROM s.CAPACIDAD);

    INSERT INTO CONFORMED.DIM_UBICACION (
        ID_UBICACION, EDIFICIO, PLANTA, ALA, SALA, TIPO_SALA, CAPACIDAD
    )
    SELECT ID_UBICACION, EDIFICIO, PLANTA, ALA, SALA, TIPO_SALA, CAPACIDAD
    FROM CLEANSED_MEDICARE.UBICACIONES
    WHERE NOT EXISTS (
        SELECT ID_UBICACION FROM CONFORMED.DIM_UBICACION
        WHERE  ID_UBICACION = CLEANSED_MEDICARE.UBICACIONES.ID_UBICACION
    );

    RETURN 'DIM_UBICACION OK';
END;
$$;


